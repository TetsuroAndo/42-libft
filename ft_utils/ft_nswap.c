#include <stddef.h>

void	ft_nswap(char *a, char *b, size_t size)
{
	char	tmp;

	if (a == b)
		return ;
	while (size--)
	{
		tmp = *a;
		*a++ = *b;
		*b++ = tmp;
	}
}
